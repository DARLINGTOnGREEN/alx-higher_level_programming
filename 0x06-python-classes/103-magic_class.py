#!/usr/bin/python3
"""define a class called magiC class"""
class MagicClass:
    def __init__(self, radius=0):
        self._MagicClass__radius = 0

    def area(self):
        return self._MagicClass__radius ** 2

    def circumference(self):
        return 2 * 3.141592653589793 * self._MagicClass__radius

